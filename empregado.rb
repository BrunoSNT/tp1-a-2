class Empregado 
    def initialize nome, sobrenome, nss   
      @nome = nome
      @sobrenome = sobrenome
      @nss = nss  
    end
    
    def nome 
        @nome
    end

    def sobrenome
        @sobrenome
    end

    def nss
        @nss
    end

    def salario
        @salario
    end

    def nome= nome 
        @nome = nome
    end

    def sobrenome= sobrenome
        @sobrenome = sobrenome
    end

    def nss= nss
        @nss = nss
    end

    def salario= salario
        @salario = salario
    end
end  

    
